# Составить функцию решения задачи: из заданного числа вычли сумму его цифр. Из
# результата вновь вычли сумму его цифр и т. д. Через сколько таких действий
# получится нуль?

def sum_of_digits(n):
    total = 0  # Инициализация переменной для хранения суммы цифр числа
    while n > 0:
        # Добавление последней цифры к сумме числа
        total += n % 10  # Извлечение последней цифры числа
        n //= 10  # Отбрасываем последнюю цифру числа
    return total  # Возвращение суммы цифр числа

# Основная функция для подсчета шагов до нуля
def steps_to_zero(number):
    steps = 0  # Счетчик шагов
    while number > 0:
        # Вычитание суммы цифр из числа на каждом шаге
        number -= sum_of_digits(number)  # Число уменьшается на сумму его цифр
        steps += 1  # Увеличение счетчика шагов на 1
    return steps  # Возвращение количества шагов

# Ввод числа с обработкой исключений (проверка, что введено положительное число)
num = input("Введите целое положительное число: ")
while not num.isdigit():  # Проверка, что введено корректное положительное число
    print("Ошибка! Введите корректное целое число.")
    num = input("Введите целое положительное число: ")
num = int(num)  # Преобразование строки в число

# Вывод результата
print(f"Число шагов до нуля: {steps_to_zero(num)}")  # Вывод количества шагов до нуля
# Дано вещественное число X и целое число N (> 0). Найти значение выражения X - X^3/(3!) + X^5/(5!) - ... + (-1)^N-X^(2-N+1)/((2-N+1)!) (N! = 12 ...N). 
# Полученное число является приближенным значением функции sin в точке X.

import math  # Подключение библиотеки math для работы с факториалом

# Ввод вещественного числа X
x = input("Введите вещественное число X: ")
# Ввод целого числа N (> 0)
n = input("Введите целое число N (> 0): ")

# Обработка исключений для X: проверка, что введённое значение - число с плавающей точкой
while True:
    try:
        x = float(x)  # Попытка преобразовать ввод в тип float
        break  # Если получилось - выход из цикла
    except ValueError:  # Ошибка в случае некорректного ввода
        print("Ошибка! Введите корректное вещественное число.")  # Сообщение об ошибке
        x = input("Введите вещественное число X: ")  # Повторный ввод

# Обработка исключений для N: проверка, чтобы N было целым и больше 0
while True:
    try:
        n = int(n)  # Попытка преобразовать ввод в тип int
        if n > 0:  # Проверка, что число больше 0
            break  # Выход из цикла в случае отсутствия ошибок 
            print("Число N должно быть больше 0.")  # Сообщение о том, что число < = 0
    except ValueError:  # Ошибка в случае некорректного ввода
        print("Ошибка! Введите целое число.")  # Сообщение об ошибке
    n = input("Введите целое число N (> 0): ")  # Повторный ввод

# Переменная для хранения результата суммы
result = 0

# Знак текущего слагаемого: начало с положительного (первый член положительный)
sign = 1

# Цикл для вычисления суммы ряда. В цикле - перебор значений от 0 до N-1
for i in range(n):
    power = 2 * i + 1  # Степень для X в текущем слагаемом (формула: 2i + 1)
    factorial = math.factorial(power)  # Вычисление факториала степени
    term = sign * (x ** power) / factorial  # Вычисление текущего слагаемого
    result += term  # Добавление его к сумме
    sign *= -1  # Замена знака для следующего слагаемого

# Вывод результата
print(f"Приближенное значение sin({x}) = {result}")